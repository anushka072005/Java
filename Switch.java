public class Switch {
    public static void main(String[] args) {
        int number = 3;

        switch (number) {
            case 1: System.out.println("Pizza");
                        break;
            
            case 2 : System.out.println("Momos");
                        break;
            
            case 3 : System.out.println("Sweet");
                        break;

            default : System.out.println("we wake up");
        }
    }
}