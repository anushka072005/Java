public class Switch2 {
    public static void main(String args[]){
        char ch = 'R';

        switch (ch) {
            case 'A': System.out.println("Anushka");
                break;

            case 'B' : System.out.println("Bhumi");
                break;

            case 'S' : System.out.println("Shivansh");
                break;

            default: System.out.println("No One");
                break;
        } 
    }   
}
